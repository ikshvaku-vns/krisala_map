function Side({ selectedTitle, handleTitleClick }) {
  const handleShowAll = () => {
    if (selectedTitle.length === Title.length) {
      // Deselect all
      Title.forEach((title) => {
        if (selectedTitle.includes(title)) {
          handleTitleClick(title); // Only deselect if already selected
        }
      });
    } else {
      // Select all
      Title.forEach((title) => {
        if (!selectedTitle.includes(title)) {
          handleTitleClick(title); // Only select if not already selected
        }
      });
    }
  };

  return (
    <div
      className="absolute top-1/2 -translate-y-1/2 left-0 flex flex-col items-center rounded-md text-sm z-10 p-1 pt-1.5 w-fit overflow-hidden h-78"
      style={{
        color: "white",
        background: "rgba(70, 69, 69, 0.5)",
        border: "2px solid rgba(70, 69, 69, 0.5)",
      }}
    >
      <button
        onClick={handleShowAll}
        className="mt-1"
        style={{
          marginBottom: "0.5rem",
          width: "70%",
          height: "auto",
          fontSize: "0.7rem",
          borderRadius: "0.5rem",
          backgroundColor:
            selectedTitle.length === Title.length ? "#dadada" : "rgba(70, 69, 69, 0.8)", // Change to green when active
          color: "white",
          textAlign: "center",
          padding: "0.5rem",
        }}
      >
        Show All
      </button>
      <div
        className="listbtn"
        style={{
          width: "auto",
          height: "auto",
          overflowY: "scroll",
          overflowX: "hidden",
          padding: "0.5rem",

          display: "flex",
          flexDirection: "column",
          color: "white",
          background: "rgba(70, 69, 69, 0.7)",
          fontSize: "0.7rem",
          borderRadius: "0.5rem",
        }}
      >
        {Title.map((titles, index) => (
          <button
            key={titles}
            onClick={() => handleTitleClick(titles)}
            style={{
              marginTop: "0.1rem",
              width: "10.5vw",
              height: "auto",
              padding: "0.1rem",
              fontSize: "0.7rem",
              borderRadius:
                index === 0
                  ? "0.5rem 0.5rem 0 0"
                  : index === Title.length - 1
                  ? "0 0 0.5rem 0.5rem"
                  : "0", // Conditional border radius
              backgroundColor: selectedTitle.includes(titles)
                ? activeColors[titles]
                : "rgba(70, 69, 69, 0.8)",
              color: "white",
              textAlign: "start",
              display: "flex",
              border: "none",
              flexDirection: "column",
              alignItems: "center",
              justifyContent: "start",
              padding: "0.5rem",
            }}
          >
            <div className="w-full flex items-center justify-between pl-1 pr-1">
              <div
                className="flex-grow text-left"
                style={{
                  color: "white",
                  alignItems: "start",
                  justifyContent: "flex-start",
                }}
              >
                {titles.charAt(0).toUpperCase() + titles.slice(1)}
              </div>
              <div className="icon ">{iconMap[titles]}</div>
            </div>
          </button>
        ))}
      </div>
    </div>
  );
}

